#include <iostream>
using namespace std;
int main()
{
    int a;
    cin >> a;
    int b;
    cin >> b;
    cout << "(a|b) = "<<(a|b)<<endl;
    cout << "(a&b) = "<<(a&b)<<endl;
    cout << "(a<<b) = "<<(a<<b)<<endl;
    cout << "(a>>b) = "<<(a>>b)<<endl;
    cout << "(~a) = "<<(~a)<<endl;
}